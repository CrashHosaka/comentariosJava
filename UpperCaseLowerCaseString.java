public class UpperCaseLowerCaseString {
    public static void main(String[] args) {
        String txt = "Hello World";
        System.out.println(txt.toUpperCase());//imprime o que está armazenado na variável "txt" com todas as letras em maiúsculo 
        System.out.println(txt.toLowerCase());//imprime o que está armazenado na variável "txt" com todas as letras em minúsculo
    }
}
