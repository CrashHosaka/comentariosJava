public class DoubleType {
    public static void main(String[] args) {
        double myNum = 19.99d;//double importa números com decimais e aguenta até 64 bits
        double myNumDouble = myNum * 2;//conta de multiplicação com a variável anterior
        System.out.println(myNumDouble/3);//divide o que está armazenado na variável "myNumDouble",divide por 3 e imprime o resultado
        
        System.out.println(myNum);
    }
}
