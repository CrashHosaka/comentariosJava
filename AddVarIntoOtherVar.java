public class AddVarIntoOtherVar {
    public static void main(String[] args) {
        String firstName = "John ";//Definiu a primeira variável
        String lastName = "Doe";//Definiu a segunda variável
        String fullName = firstName + lastName;//Criou uma variável que junta as outras duas variáveis em uma só
        System.out.println(fullName);//Mostra a variável final
      }
}
